// Debug path recorder for On Rails mode — enabled with ?record. It samples the
// player's XZ position + FSM state through one clean free-movement playthrough,
// and at the credits downloads a ready-to-drop-in src/rails-path.js. The On Rails
// replay (rails.js) reads that file, so your capture run becomes everyone's path.
//
// Never active in the playable build (gated by ?record, like ?debug). Recording
// is always done in FREE movement (main forces onRails off while ?record is set).

const SAMPLE_DT = 0.08;                         // ~12.5 Hz; linearly interpolated on playback
const RND = (v, d) => Math.round(v * 10 ** d) / 10 ** d;

export class RailsRecorder {
  constructor() {
    this.samples = [];
    this.t = 0;
    this.acc = SAMPLE_DT;
    this.on = false;
    this.done = false;
  }

  // Called from TRAIN_SAFE enter — resets so a replayed run re-captures cleanly.
  start() {
    this.samples = [];
    this.t = 0;
    this.acc = SAMPLE_DT;
    this.on = true;
    this.done = false;
  }

  update(dt, state, pos) {
    if (!this.on || this.done) return;
    this.t += dt;
    this.acc += dt;
    if (this.acc < SAMPLE_DT) return;
    this.acc = 0;
    this.samples.push({ t: RND(this.t, 2), x: RND(pos.x, 3), z: RND(pos.z, 3), s: state });
  }

  // Called from CREDITS enter — serialize + download src/rails-path.js.
  finish() {
    if (this.done || !this.on) return;
    this.done = true;
    this.on = false;

    const reachedCharger = this.samples.some((s) => s.s === 'FALSE_RELIEF');
    const climbed = this.samples.some((s) => s.s === 'ENDING' && s.z < -12.5);
    if (!reachedCharger) console.warn('[rails] capture never reached the charger / FALSE_RELIEF — incomplete. Re-run and charge to completion.');
    if (!climbed) console.warn('[rails] capture never crossed the stair vestibule (z < -12.5) — the climb won’t trigger on rails. Re-run and walk fully into the exit.');

    const body =
      '// Recorded canonical camera path for On Rails mode (read by rails.js).\n' +
      '// Generated by ?record — one clean free-movement playthrough.\n' +
      `export const railPath = ${JSON.stringify(this.samples)};\n`;

    try {
      const url = URL.createObjectURL(new Blob([body], { type: 'text/javascript' }));
      const a = document.createElement('a');
      a.href = url;
      a.download = 'rails-path.js';
      document.body.appendChild(a);
      a.click();
      a.remove();
      setTimeout(() => URL.revokeObjectURL(url), 1000);
    } catch (_) { /* download blocked → use the console copy below */ }

    console.log(`[rails] captured ${this.samples.length} samples over ${RND(this.t, 1)}s. ` +
      `Downloaded rails-path.js — move it into src/. Full contents below if the download was blocked:\n\n${body}`);
  }
}
